_AS_OF = {"batting": "2026-05-24", "pitching": "2026-05-14"}


def rows_from(lines, header):
    out = []
    n_num = len(header) - 2  # 順位/名前以外の数値列数
    for line in lines:
        toks = line.split()
        rank = toks[0]
        nums = toks[-n_num:]
        name = " ".join(toks[1:len(toks) - n_num])
        out.append(dict(zip(header, [rank, name] + nums)))
    return out


def build(lines, header, group, league, qualified=True, seen=None):
    players = []
    if seen is None:
        seen = set()
    for rec in rows_from(lines, header):
        name = f.split_name_team(rec[header[1]])[0]
        if name in seen:
            continue
        seen.add(name)
        p = f.build_player_record(rec, group, league, FETCHED_AT, _AS_OF[group], qualified)
        if p:
            players.append(p)
    return players


